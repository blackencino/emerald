//-*****************************************************************************
//-*****************************************************************************
// This iterator class will consume the ints in the exhaustive LUT table.
// It is only used by the Variation class to update itself.
//-*****************************************************************************
//-*****************************************************************************
class LUTIterator
{
public:
    LUTIterator() : m_streamPos( 0 ) {}

    bool valid() const
    {
        return ( m_streamPos < g_NumInLUT );
    }

    operator bool() const { return valid(); }

    int operator*() const
    {
        if ( !valid() )
        {
            EMLD_THROW( "ElutCell: Reached end of LUT. Eek" );
        }
        return g_ExhaustiveLUT[m_streamPos];
    }

    void operator++() { ++m_streamPos; }
    void operator++( int ) { ++m_streamPos; }

    void throwCorrupt( const std::string& iMsg ) const
    {
        EMLD_THROW( "ElutCell::LUTIterator LUT corruption.\n"
                    << "Reason: " << iMsg << std::endl
                    << "Stream Pos: " << m_streamPos
                    << " and LUT[i]: " << g_ExhaustiveLUT[m_streamPos] );
    }


protected:
    int m_streamPos;
};

//-*****************************************************************************
//-*****************************************************************************
// VARIATION
// The Exhaustive LUT is just a serialization of these Variation classes.
//-*****************************************************************************
//-*****************************************************************************

//-*****************************************************************************
void Variation::readFromInts( LUTIterator& io_liter )
{
    // read the first seven ints into our saddle
    for ( int i = 0; i < 7; ++i, ++io_liter )
    {
        m_saddle.data[i] = ( *io_liter );
        if ( m_saddle.data[i] != -1 &&
             m_saddle.data[i] != 0 &&
             m_saddle.data[i] != 1 )
        {
            io_liter.throwCorrupt( "Bad saddle point not -1,0,1" );
        }
    }

    // Read our num tris
    int numTris = ( *io_liter );
    if ( numTris < 1 )
    {
        io_liter.throwCorrupt( "Bad num tris, should be more than 0" );
    }
    ++io_liter;

    m_triangles.resize( numTris );
    for ( int i = 0; i < numTris; ++i )
    {
        V3i& tri = m_triangles[i];
        for ( int j = 0; j < 3; ++j, ++io_liter )
        {
            tri[j] = ( *io_liter );
            if ( tri[j] < 1 || tri[j] > 32 )
            {
                io_liter.throwCorrupt( "Triangle indices must be in 1-32" );
            }
        }
    }
}

//-*****************************************************************************
//-*****************************************************************************
// VARIATIONS CLASS
//-*****************************************************************************
//-*****************************************************************************

//-*****************************************************************************
void Variations::readFromInts( std::size_t i_index, LUTIterator& io_liter )
{
    m_index = i_index;

    int idx = ( *io_liter );
    if ( idx != m_index )
    {
        io_liter.throwCorrupt(
            ( boost::format( "Expected index: %d" ) % m_index ).str() );
    }
    ++io_liter;

    int numVars = ( *io_liter );
    ++io_liter;

    m_variations.resize( numVars );
    for ( int i = 0; i < numVars; ++i )
    {
        m_variations[i].readFromInts( io_liter );
    }
}

//-*****************************************************************************
//-*****************************************************************************
// LUT CLASS
//-*****************************************************************************
//-*****************************************************************************

//-*****************************************************************************
LUT::LUT()
{
    m_variations.resize( 256 );
    LUTIterator liter;
    for ( int i = 0; i < 256; ++i )
    {
        m_variations[i].readFromInts( i, liter );
    }
    if ( ( *liter ) != -3 )
    {
        EMLD_THROW( "Error reading LUT. Didn't get to end." );
    }
}

